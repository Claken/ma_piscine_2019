void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	t;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		t = tab[i];
		tab[i] = tab[j];
		tab[j] = t;
		i++;
		j--;
	}
}
