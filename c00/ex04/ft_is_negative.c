#include <unistd.h>

void	ft_is_negative(int n)
{
	char	l1;
	char	l2;

	l1 = 'N';
	l2 = 'P';
	if (n < 0)
		write(1, &l1, 1);
	else
		write(1, &l2, 1);
}
