int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 0 && str[i] <= 64)
			return (0);
		if (str[i] >= 91 && str[i] <= 96)
			return (0);
		if (str[i] >= 123)
			return (0);
		i++;
	}
	return (1);
}
