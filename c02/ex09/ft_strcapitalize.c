char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	i = 0;
	while (str[i])
	{
		if (!(str[i - 1] >= 48 && str[i - 1] <= 57)
			&& !(str[i - 1] >= 65 && str[i - 1] <= 90)
			&& !(str[i - 1] >= 97 && str[i - 1] <= 122))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
